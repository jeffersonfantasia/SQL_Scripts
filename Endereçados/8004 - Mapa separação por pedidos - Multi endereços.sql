       MAX((CASE
             WHEN (NVL(PCFILIAL.UTILIZAVENDAPOREMBALAGEM,
                       NVL(PCCONSUM.UTILIZAVENDAPOREMBALAGEM, 'N')) = 'S') THEN
              (SELECT PCEMBALAGEM.PESOBRUTO
