         E.NIVEL,
         E.NAPTO,
         G.PROD_ID   AS CODPROD,
         G.QTD       AS QT_ENDERECADO
    FROM JCENDESTOQUE G
    LEFT JOIN ENDERECAMENTO E
      ON G.APTO_ID = E.APTO_ID)
