SELECT DTMOV,
       CODCLI,
       CODPROD,
       SUM (QTCONT) AS QT_TOTAL
  FROM (
    SELECT M.DTMOV,
           S.CODCLI,
           M.CODPROD,
           M.QTCONT
      FROM PCMOV M
     INNER JOIN PCNFSAID S ON M.NUMTRANSVENDA = S.NUMTRANSVENDA
      LEFT JOIN VIEW_JC_VENDEDOR V ON S.CODUSUR = V.CODUSUR
     WHERE M.CODFISCAL IN (
        5102, 5109, 5119, 5120, 5403, 5405, 6102, 6108, 6403, 6119, 6120, 5910, 6910
    )
       AND V.CODGERENTE IN (
        1, 8, 9, 10
    )
       AND M.DTMOV >= TO_DATE ('01/01/2020', 'DD/MM/YYYY')
       AND M.DTCANCEL IS NULL
)
 GROUP BY DTMOV,
          CODCLI,
          CODPROD;
