/*---------------------VALOR DA ULTIMA ENTRADA DE COMPRA----------------*/
WITH ULTIMAENTRADA_ESTRELA AS (
    SELECT MAX (M.DTMOV) AS MAX_DTMOV,
           M.CODPROD,
           M.PUNIT
      FROM PCMOV M
     INNER JOIN PCNFENT E ON M.NUMTRANSENT = E.NUMTRANSENT
     WHERE M.CODFILIAL = 2
       AND E.CODFORNEC = 9720
       AND M.CODPROD IN (
        811630,
814408,
811431,
811432,
814345,
811191,
811399,
811423,
811387,
811424,
811389,
811398,
811401,
811386,
811433,
814174,
815169,
811447,
814352,
809260
    )
     GROUP BY M.CODPROD,
              M.PUNIT
)
SELECT DISTINCT M.DTMOV,
                M.CODFILIAL,
                M.CODPROD,
                P.DESCRICAO,
                M.NUMNOTA,
                M.PUNIT
  FROM PCMOV M
  LEFT JOIN PCPRODUT P ON M.CODPROD = P.CODPROD
 INNER JOIN PCNFENT E ON M.NUMTRANSENT = E.NUMTRANSENT
 INNER JOIN ULTIMAENTRADA_ESTRELA U ON M.CODPROD = U.CODPROD
   AND M.DTMOV = U.MAX_DTMOV
 WHERE M.CODFILIAL = 2
   AND E.CODFORNEC = 9720
 ORDER BY M.CODPROD,
          M.DTMOV
/

