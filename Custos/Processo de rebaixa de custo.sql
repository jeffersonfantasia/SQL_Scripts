/*----------------------------ESTOQUE E CUSTO NAS FILIAL----------------*/
SELECT *
  FROM (
    SELECT T.CODFILIAL,
           T.CODPROD,
           NVL (T.QTESTGER, 0) AS QTESTGER,
           NVL (T.VALORULTENT, 0) AS VALORULTENT,
           NVL (T.CUSTOFIN, 0) AS CUSTOFIN
      FROM PCEST T
     WHERE T.CODPROD IN (
        811630,
814408,
811431,
811432,
814345,
811191,
811399,
811423,
811387,
811424,
811389,
811398,
811401,
811386,
811433,
814174,
815169,
811447,
814352,
809260
    )
) PIVOT (
    SUM (QTESTGER)
QTESTGER, SUM (VALORULTENT) VALORULTENT, SUM (CUSTOFIN) CUSTOFIN
    FOR CODFILIAL
    IN (1, 2, 3, 4, 7, 8)
)
 ORDER BY CODPROD;
/


