SELECT F.CODFILIAL,
       F.DTSAIDA,
       F.NUMPED,
       C.NUMPEDWEB,
       F.NUMTRANSVENDA,
       F.NUMNOTA,
       F.NUMVOLUME,
       F.CODUSUR,
       I.NOME
  FROM PCNFSAID F,
       PCPEDC C,
       PCUSUARI I
 WHERE F.CODSUPERVISOR IN (
    7, 8
)
   AND F.NUMVOLUME > 1
   AND C.NUMTRANSVENDA = F.NUMTRANSVENDA
   AND I.CODUSUR = F.CODUSUR
   AND F.DTCANCEL IS NULL
   AND F.DTSAIDA BETWEEN TO_DATE ('04/10/2018', 'DD/MM/YYYY') AND TO_DATE ('10/10/2018', 'DD/MM/YYYY')
 ORDER BY F.DTSAIDA DESC;
