SELECT P.DTFAT,
       P.DATA,
       P.NUMPED,
       P.NUMPEDRCA,
       P.CODCLI,
       C.CLIENTE,
       C.CGCENT CPF,
       C.EMAIL,
       P.NUMVOLUME,
       P.CODUSUR,
       U.NOME
  FROM PCPEDC P,
       PCCLIENT C,
       PCUSUARI U
 WHERE P.CODCLI = C.CODCLI
   AND P.CODUSUR = U.CODUSUR
   AND P.CODSUPERVISOR IN (
    7, 8
)
   AND P.NUMVOLUME > 1
   AND P.POSICAO = 'F'
   AND P.DTFAT > TRUNC (SYSDATE) - 2
  /* AND P.DTFAT BETWEEN TO_DATE('01-09-2018', 'DD/MM/YYYY') AND TO_DATE('01-12-2018', 'DD/MM/YYYY')*/
 ORDER BY P.DTFAT;
