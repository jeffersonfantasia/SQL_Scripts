SELECT C.DATA,
       C.NUMPED,
       C.CODCLI,
       T.CLIENTE,
       I.CODPROD,
       D.DESCRICAO,
       I.QT
  FROM PCPEDI I
 INNER JOIN PCPEDC C ON I.NUMPED = C.NUMPED
  LEFT JOIN PCSUPERV V ON C.CODSUPERVISOR = V.CODSUPERVISOR
  LEFT JOIN PCCLIENT T ON C.CODCLI = T.CODCLI
  LEFT JOIN PCPRODUT D ON I.CODPROD = D.CODPROD
 WHERE C.POSICAO NOT IN (
    'C', 'F'
)
   AND V.CODGERENTE IN (
    1, 8, 9
)
   AND EXISTS (
    SELECT CODPROD
      FROM PCPRODUT P
     WHERE P.CODPROD IN (
        809243, 807779, 807781, 805833, 809259, 806961, 806629, 806622, 806627, 806625, 809258, 805835, 801921, 806623, 801268, 806632
        , 806621, 809257, 809261, 797327, 803084, 806964, 806962, 806963
    )
       AND I.CODPROD = P.CODPROD
)
 ORDER BY C.NUMPED;
